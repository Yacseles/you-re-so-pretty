let currentLevel = 0;
let playerPosition = { x: 1, y: 1 };

function createMaze(level) {
    const mazeContainer = document.getElementById('maze');
    mazeContainer.innerHTML = ''; // Clear previous maze

    // Retrieve maze configuration for the current level
    const maze = levels[level].maze;

    // Loop through maze configuration and create HTML elements
    for (let row = 0; row < maze.length; row++) {
        for (let col = 0; col < maze[row].length; col++) {
            const cell = document.createElement('div');
            cell.classList.add('cell');

            // Set appropriate classes based on maze configuration
            if (maze[row][col] === 1) {
                cell.classList.add('wall');
            } else if (playerPosition.x === col && playerPosition.y === row) {
                cell.classList.add('player');
            } else if (levels[level].exitPosition.x === col && levels[level].exitPosition.y === row) {
                cell.classList.add('exit');
            }

            // Append cell to maze container
            mazeContainer.appendChild(cell);
        }
    }

    // Update the level number displayed on the page
    document.getElementById('levelNumber').textContent = (level + 1).toString();
}

function movePlayer(dx, dy) {
    const newX = playerPosition.x + dx;
    const newY = playerPosition.y + dy;

    // Check if the new position is within bounds and not a wall
    if (levels[currentLevel].maze[newY][newX] === 0) {
        playerPosition.x = newX;
        playerPosition.y = newY;
        createMaze(currentLevel); // Redraw the maze with updated player position
        checkWin(); // Check if the player has reached the exit
    }
}

function checkWin() {
    const messageDiv = document.getElementById('message');
    const nextPageButton = document.getElementById('nextPageButton');

    if (playerPosition.x === levels[currentLevel].exitPosition.x &&
        playerPosition.y === levels[currentLevel].exitPosition.y) {

        // Custom messages based on the level
        let customMessage = '';
        switch (currentLevel) {
            case 0:
                customMessage = "You've completed Level 1! good job!! maybe you're not a baby (JK YOU ARE HEHEHE):)";
                break;
            case 1:
                customMessage = "Well done!!! YOU'RE SO GOOD AT THIS OMDSSS <33";
                break;
            case 2:
                customMessage = "HOW ARE YOU SO GOOD AT THIS!!! YOU'RE A PRO L^0^/";
                break;
            case 3:
                customMessage = "Level 4 completed...you're so perfect!!!Literally a gamer girl <333";
                break;
            case 4:
                customMessage = "CONGRATS MIYA you completed all levels, I'm so proud :D PRESS THE BUTTON FOR OUR SECRET PAGE! HEHE";
                // Show the button to go to the next page
                nextPageButton.style.display = 'inline-block';
                break;
            default:
                customMessage = "Congratulations!";
        }

        // Display the custom message
        messageDiv.textContent = customMessage;

        // Increment level or show completion message
        currentLevel++;
        if (currentLevel < levels.length) {
            playerPosition = { ...levels[currentLevel].playerStart };
            createMaze(currentLevel); // Update the maze for the next level
        } else {
            messageDiv.textContent = "CONGRATS MIYA you completed all levels, I'm so proud :D PRESS THE BUTTON FOR OUR SECRET PAGE! HEHE";
            // Show the button to go to the next page
            nextPageButton.style.display = 'inline-block';
        }
    }
}

